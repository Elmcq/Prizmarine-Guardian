export function findCity(query) {
  const q = query.toLowerCase().trim();
  if (INDONESIAN_CITIES[q]) return INDONESIAN_CITIES[q];
  const results = [];
  for (const [key, city] of Object.entries(INDONESIAN_CITIES)) {
    if (key.includes(q) || city.name.toLowerCase().includes(q)) {
      results.push({ key, ...city });
    }
  }
  return results.length > 0 ? results[0] : null;
}
